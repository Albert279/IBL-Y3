# This code is asking the user to input a greeting and storing it in the variable `grt`. It then
# checks if the input is exactly equal to the string "hello" and prints "" if it is. If the input
# starts with the letter "h", it prints "". If neither of these conditions are met, it prints
# "".
grt = str(input("what is your greeting?: "))
if grt== "hello":
    print("$0")
elif grt [0]== "h":
    print("$20")
else:
    print("$100")